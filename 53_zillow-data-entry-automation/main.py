form_url = os.environ["google_form"]
header = {
    "User-Agent": "Mozilla/5.0 (X11; Linux x86_64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/111.0.0.0 Safari/537.36"
}

response = requests.get(zillow_url, headers=header).text
soup = BeautifulSoup(response, "lxml")

listings = soup.find_all("div", {"class" : re.compile("StyledPropertyCardDataWrapper.* ")})

urls = []
addresses = []
prices = []

for listing in listings:
    url_element = listing.find("a", class_="property-card-link")
    if url_element["href"].startswith("/"):
        url = f"http://www.zillow.com{url_element['href']}"
    else:
        url = url_element["href"]
    urls.append(url)

    address = url_element.find("address").text
    addresses.append(address)

    price = url_element.find_next("span").text
    prices.append(price)

# inputting into Google form

CHROME_DRIVER_PATH = "/home/anna/Applications/chromedriver"
chrome_options = webdriver.ChromeOptions()
chrome_options.add_argument('--no-sandbox')
chrome_options.add_argument('--headless')
chrome_options.add_argument('--disable-gpu')
driver = webdriver.Chrome(service=Service(CHROME_DRIVER_PATH), options=chrome_options)

for i in range(len(listings)-1):
    driver.get(form_url)
    sleep(1)
    address_input = driver.find_element(By.XPATH,  '//*[@id="mG61Hd"]/div[2]/div/div[2]/div[1]/div/div/div[2]/div/div[1]/div/div[1]/input')
    price_input = driver.find_element(By.XPATH, '//*[@id="mG61Hd"]/div[2]/div/div[2]/div[2]/div/div/div[2]/div/div[1]/div/div[1]/input')
    link_input = driver.find_element(By.XPATH, '//*[@id="mG61Hd"]/div[2]/div/div[2]/div[3]/div/div/div[2]/div/div[1]/div/div[1]/input')
    submit = driver.find_element(By.XPATH, '//*[@id="mG61Hd"]/div[2]/div/div[3]/div[1]/div[1]/div/span/span')

    address_input.send_keys(addresses[i])
    price_input.send_keys(prices[i])
    link_input.send_keys(urls[i])
    submit.click()